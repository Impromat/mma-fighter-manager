const I18N = {
  _lang: 'fr',

  init() {
    this._lang = localStorage.getItem('mma_lang') || 'fr';
  },

  getLang() { return this._lang; },

  setLang(lang) {
    this._lang = lang;
    localStorage.setItem('mma_lang', lang);
  },

  /**
   * Translate a key with optional parameter replacement
   * t('dashboard.title') => 'Tableau de bord'
   * t('week.label', { n: 5 }) => 'Semaine 5'
   */
  t(key, params) {
    const lang = this._lang;
    const val = LANG[lang]?.[key] ?? LANG['en']?.[key] ?? key;
    if (!params) return val;
    let result = val;
    Object.entries(params).forEach(([k, v]) => {
      result = result.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    });
    return result;
  }
};

